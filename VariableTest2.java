public class VariableTest2 {
    //主入口
    public static void main(String[] args) {
        //定义变量记录姓名
        String name = "黑马";
        System.out.println(name);
        //年龄
        int age = 18;
        System.out.println(age);
        //性别
        char gender = '男';
        System.out.println(gender);
        //身高
        double height = 180.1;
        System.out.println(height);
        //true 单身 false 非单身
        boolean flag = true;
        System.out.println(flag);

        //输出变量的值
        System.out.println(name);
        System.out.println(age);
        System.out.println(gender);
        System.out.println(height);
        System.out.println(flag);




    }
}
